/*
Graph Interview Questions
---------------------------
1. What is a graph?
A data structure consisting of vertices (nodes) and edges (connections).

2. Types of graphs?
a. Directed
b. Undirected
c. Weighted
d. Unweighted
e. Cyclic
f. Acyclic

3. What is degree of vertex?
Number of edges connected to a vertex.
Directed → In-degree + Out-degree.

4. What is path?
Sequence of vertices connected by edges.

5. What is cycle?
Path starting and ending at same vertex.

6. What is connected graph?
Every node reachable from every other node.

7. What is tree?
Connected acyclic graph with N nodes → N−1 edges.

8. Graph representation methods?
a. Adjacency Matrix
b. Adjacency List

9. What is BFS?
Level-order traversal using Queue.

10. What is DFS?
Depth traversal using Stack / Recursion.

11. BFS time complexity?
O(V + E)

12. DFS time complexity?
O(V + E)

13. What is Topological Sort?
Linear ordering of vertices in DAG (Directed Acyclic Graph).

14. What is Dijkstra algorithm?
Find shortest path from source (no negative weights).

15. What is Bellman-Ford?
Shortest path, supports negative weights.

16. What is MST?
Minimum Spanning Tree → Minimum cost to connect all nodes.

17. MST algorithms?
a. Kruskal
b. Prim

18. What is Union Find (DSU)?
Efficient structure for connected components detection.

19. What is Strongly Connected Component?
In directed graph, every node reachable from every other node.

20. BFS vs DFS main difference?
a. BFS → Level based
b. DFS → Depth based
*/
